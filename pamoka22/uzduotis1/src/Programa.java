//     2  main funkcijoje išveskite bet kokį skaičių į konsolę.
//     3  main funkcijoje išveskite bet kokį tekstą į konsolę.
//     4  main funkcijoje išveskite žodį namas į konsolę.
//     5  main funkcijoje išveskite bet kokį dviejų skaičių sumą į konsolę.
//     6  main funkcijoje išveskite bet kokių dviejų tekstų sumą atskirtą tarpu į konsolę.
public class Programa {
    // 1 punktas yra projekto sukūrimas ir šitos main funkcijos sukūrimas
    public static void main(String[] args) {
        // 2 punktas
        System.out.println(47);
        // 3 punktas
        System.out.println("Penktadienis");
        // 4 punktas
        System.out.println("namas");
        // 5 punktas
        System.out.println(5 + 17);
        // 6 punktas
        System.out.println("Šiandien" + " " + "penktadienis");
    }
}
